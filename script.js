// DOM 元素
const audioPlayer = document.getElementById("audioPlayer");
const audioSource = document.getElementById("audioSource");
const playPauseBtn = document.getElementById("playPauseBtn");
const prevBtn = document.getElementById("prevBtn");
const nextBtn = document.getElementById("nextBtn");
const currentSongTitle = document.getElementById("currentSongTitle");
const currentSongIndex = document.getElementById("currentSongIndex");
const songListContainer = document.getElementById("songListContainer");
const lyricsContent = document.getElementById("lyricsContent");
const conceptContent = document.getElementById("conceptContent");

// 當前播放狀態
let currentSongIdx = -1;
let isPlaying = false;

// 初始化
function init() {
    renderSongList();
    setupEventListeners();
}

// 渲染歌曲列表
function renderSongList() {
    songListContainer.innerHTML = "";
    
    songs.forEach((song, index) => {
        const songItem = document.createElement("div");
        songItem.className = "song-item";
        songItem.innerHTML = `
            <h4>${song.title}</h4>
            <p>第 ${index + 1} 首</p>
        `;
        
        songItem.addEventListener("click", () => {
            selectSong(index);
        });
        
        songListContainer.appendChild(songItem);
    });
}

// 選擇歌曲
function selectSong(index) {
    if (index < 0 || index >= songs.length) return;
    
    currentSongIdx = index;
    const song = songs[index];
    
    // 更新音頻源
    audioSource.src = song.audio;
    audioPlayer.load();
    
    // 更新UI
    updateSongInfo();
    updateSongList();
    updateLyrics();
    updateConcept();
}

// 更新歌曲資訊
function updateSongInfo() {
    if (currentSongIdx >= 0) {
        currentSongTitle.textContent = songs[currentSongIdx].title;
        currentSongIndex.textContent = `${currentSongIdx + 1} / ${songs.length}`;
    }
}

// 更新歌曲列表樣式
function updateSongList() {
    const songItems = document.querySelectorAll(".song-item");
    songItems.forEach((item, index) => {
        item.classList.toggle("active", index === currentSongIdx);
    });
}

// 更新歌詞
function updateLyrics() {
    if (currentSongIdx >= 0) {
        const lyrics = songs[currentSongIdx].lyrics;
        lyricsContent.innerHTML = lyrics.split("\n").map(line => 
            line.trim() ? `<p>${line}</p>` : `<br>`
        ).join("");
    } else {
        lyricsContent.innerHTML = `<p class="placeholder">請選擇歌曲查看歌詞</p>`;
    }
}

// 更新創作理念
function updateConcept() {
    if (currentSongIdx >= 0) {
        const concept = songs[currentSongIdx].concept;
        conceptContent.innerHTML = concept.split("\n").map(line => 
            line.trim() ? `<p>${line}</p>` : `<br>`
        ).join("");
    } else {
        conceptContent.innerHTML = `<p class="placeholder">請選擇歌曲查看創作理念</p>`;
    }
}

// 播放/暫停
function togglePlayPause() {
    if (currentSongIdx < 0) {
        selectSong(0);
        return;
    }
    
    if (isPlaying) {
        audioPlayer.pause();
    } else {
        audioPlayer.play();
    }
}

// 上一首
function prevSong() {
    if (currentSongIdx > 0) {
        selectSong(currentSongIdx - 1);
        if (isPlaying) {
            audioPlayer.play();
        }
    }
}

// 下一首
function nextSong() {
    if (currentSongIdx < songs.length - 1) {
        selectSong(currentSongIdx + 1);
        if (isPlaying) {
            audioPlayer.play();
        }
    }
}

// 設置事件監聽器
function setupEventListeners() {
    // 播放器事件
    audioPlayer.addEventListener("play", () => {
        isPlaying = true;
        playPauseBtn.textContent = "⏸";
    });
    
    audioPlayer.addEventListener("pause", () => {
        isPlaying = false;
        playPauseBtn.textContent = "▶";
    });
    
    audioPlayer.addEventListener("ended", () => {
        nextSong();
    });
    
    // 控制按鈕事件
    playPauseBtn.addEventListener("click", togglePlayPause);
    prevBtn.addEventListener("click", prevSong);
    nextBtn.addEventListener("click", nextSong);
    
    // 鍵盤快捷鍵
    document.addEventListener("keydown", (e) => {
        switch(e.code) {
            case "Space":
                e.preventDefault();
                togglePlayPause();
                break;
            case "ArrowLeft":
                e.preventDefault();
                prevSong();
                break;
            case "ArrowRight":
                e.preventDefault();
                nextSong();
                break;
        }
    });
}

// 頁面載入完成後初始化
document.addEventListener("DOMContentLoaded", init);

